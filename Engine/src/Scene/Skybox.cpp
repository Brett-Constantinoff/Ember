#include "SkyBox.h"

namespace Ember
{
    namespace Scene
    {
        SkyBox::SkyBox(const char* name, const Renderer::Material& mat, std::vector<const char*>& files) :
            SceneObject{ name, mat }, m_texture{ GL_TEXTURE_CUBE_MAP, files }
        {
            m_vao.bind();
            float vertexData[] = {
                // positions          
                -1.0f,  1.0f, -1.0f,
                -1.0f, -1.0f, -1.0f,
                 1.0f, -1.0f, -1.0f,
                 1.0f, -1.0f, -1.0f,
                 1.0f,  1.0f, -1.0f,
                -1.0f,  1.0f, -1.0f,

                -1.0f, -1.0f,  1.0f,
                -1.0f, -1.0f, -1.0f,
                -1.0f,  1.0f, -1.0f,
                -1.0f,  1.0f, -1.0f,
                -1.0f,  1.0f,  1.0f,
                -1.0f, -1.0f,  1.0f,

                 1.0f, -1.0f, -1.0f,
                 1.0f, -1.0f,  1.0f,
                 1.0f,  1.0f,  1.0f,
                 1.0f,  1.0f,  1.0f,
                 1.0f,  1.0f, -1.0f,
                 1.0f, -1.0f, -1.0f,

                -1.0f, -1.0f,  1.0f,
                -1.0f,  1.0f,  1.0f,
                 1.0f,  1.0f,  1.0f,
                 1.0f,  1.0f,  1.0f,
                 1.0f, -1.0f,  1.0f,
                -1.0f, -1.0f,  1.0f,

                -1.0f,  1.0f, -1.0f,
                 1.0f,  1.0f, -1.0f,
                 1.0f,  1.0f,  1.0f,
                 1.0f,  1.0f,  1.0f,
                -1.0f,  1.0f,  1.0f,
                -1.0f,  1.0f, -1.0f,

                -1.0f, -1.0f, -1.0f,
                -1.0f, -1.0f,  1.0f,
                 1.0f, -1.0f, -1.0f,
                 1.0f, -1.0f, -1.0f,
                -1.0f, -1.0f,  1.0f,
                 1.0f, -1.0f,  1.0f
            };
            m_vbo.bind();
            m_vbo.setData(sizeof(vertexData), vertexData, GL_STATIC_DRAW);
            m_vao.setLayout(3, GL_FLOAT, GL_FALSE, sizeof(float) * 3, (void*)0);
            m_texture.load();
        }

        SkyBox::~SkyBox()
        {
        }

        Renderer::TextureCubeMap* SkyBox::getCubeMap()
        {
            return &m_texture;
        }
    }
}